package edu.ucsb.eucalyptus.cloud.entities;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.PersistenceContext;
import javax.persistence.Table;

import org.hibernate.annotations.Cache;
import org.hibernate.annotations.CacheConcurrencyStrategy;

@Entity
@PersistenceContext(name = "hippo")
@Table(name = "client_instances")
@Cache(usage = CacheConcurrencyStrategy.READ_WRITE)
public class ClientInstanceInfo implements Comparable {
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name = "instance_id")
	private Long instanceId = -1l;

	@Column(name = "instance_name")
	private String instanceName;

	@Column(name = "instance_key")
	private String instanceKey;

	@Column(name = "user_id")
	private String userId;

	@Column(name = "is_enabled")
	private Boolean isEnabled;

	@Column(name = "hardware_id")
	private String hardwareId;

	@Column(name = "create_date")
	private Date createDate;

	@Column(name = "edit_date")
	private Date editDate;

	@Override
	public int compareTo(Object arg0) {
		return this.instanceId.compareTo(((ClientInstanceInfo) arg0).getInstanceId());
	}

	public Long getInstanceId() {
		return instanceId;
	}

	public void setInstanceId(Long instanceId) {
		this.instanceId = instanceId;
	}

	public String getInstanceName() {
		return instanceName;
	}

	public void setInstanceName(String instanceName) {
		this.instanceName = instanceName;
	}

	public String getInstanceKey() {
		return instanceKey;
	}

	public void setInstanceKey(String instanceKey) {
		this.instanceKey = instanceKey;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Boolean getIsEnabled() {
		return isEnabled;
	}

	public void setIsEnabled(Boolean isEnabled) {
		this.isEnabled = isEnabled;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Date getEditDate() {
		return editDate;
	}

	public void setEditDate(Date editDate) {
		this.editDate = editDate;
	}

	public String getHardwareId() {
		return hardwareId;
	}

	public void setHardwareId(String hardwareId) {
		this.hardwareId = hardwareId;
	}

}
